public class Lab053_forLoop {
    public static void main(String[] args) {

        for(int i = 0; i<10; i++){ //0-9, so total 10 times
            System.out.println("Hello");
        }

        for (int i = 1; i<=10; i++){ //1-10, so total 10 times
            System.out.println("Hi there");
        }
    }
}
